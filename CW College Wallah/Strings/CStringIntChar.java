public class CStringIntChar {
    public static void main(String[] args) {

        // String 
        String s = "abcde";
        s = s + "fghij";
        System.out.println(s);
        // int
        int a = 10;
        a = a + 20;
        System.out.println(a);
        // char
        char c = 'a';
        c = (char) (c + 1);
        System.out.println(c);
        
    }
    
}
